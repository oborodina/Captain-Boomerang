package Homework1;

public class PrimitiveTypes {
        public static void main(String[] args) {
            byte b = 127;
            short s = 32767;
            int i = 2147473647;
            long l = 1000000L;
            double d = 32.123456789012345d;
            float f = 49.123456f;
            System.out.println(b + s);
            System.out.println(l - i);
            System.out.println(d * b);
            System.out.println(f / i);
        }
    }
